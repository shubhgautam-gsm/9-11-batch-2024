# % (reminder) It returns the reminder after dividing the first operand by the second operand
user=int(input(' write number '))
   
if user % 2 == 0:
    print(user,"is even")
elif user % 2 == 1:
    print(user,"is odd")
else:
    print(user,"invalid number")
    

